enum KeyboardLayouts {
	ENG = 0,
	UA = 1,
	RU = 2
};

enum KeyboardLayouts curlay = ENG;

void
switch_keyboard(const Arg *arg) {
	switch (curlay) {
	case ENG:
		curlay = UA;
		system("setxkbmap ua");
		break;
	case UA:
		curlay = RU;
		system("setxkbmap ru");
		break;
	case RU:
		curlay = ENG;
		system("setxkbmap us");
		break;
	}
}

